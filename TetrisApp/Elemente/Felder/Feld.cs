
namespace TetrisApp.Elements.Fields
{
    public class Field
    {
        #region VARS
        public int x;
        public int y;
        #endregion VARS

        #region CONS
        public Field()
        {
            x = 0; y = 0;
        }

        public Field(short xt, short yt)
        {
            x = xt; y = yt;
        }
        #endregion CONS
    }
}

